// Запропонуйте користувачу ввести число за допомогою prompt().
// Збережіть введене значення у змінну number.
// Перевірте, чи дорівнює змінна number числу 10:
//    Якщо так – виведіть повідомлення 'Вірно' у alert().
//    Якщо ні – виведіть 'Невірно'. 

// Підказка: prompt завжди повертає рядок (String), тому перед перевіркою  
// перетворіть значення на число за допомогою Number().
 
let input = prompt("Введіть число:");
let number = Number(input);

if (input === null || input.trim() === "" || isNaN(number)) {
  alert("Будь ласка, введіть коректне число");
} else if (number === 10) {
  alert("Вірно");
} else {
  alert("Невірно");
}  
// Перевірка на null та порожній рядок для уникнення помилок
// isNaN(number) перевіряє, чи введене значення є числом
// input.trim() видаляє пробіли на початку та в кінці рядка
// Якщо користувач натисне "Скасувати" або введе порожній рядок,
// буде виведено повідомлення про помилку.
// Це допомагає уникнути помилок при перетворенні рядка в число.    


const min = Math.floor(Math.random() * (59 - 0) + 0);
// Генеруємо випадкове число від 0 до 59; Використовуємо Math.floor для округлення до цілого числа
// Використовуємо Math.random() для генерації випадкового числа
if (min < 15) {
   alert(`${min} входить в першу чверть`);// Перевіряємо, в яку чверть входить число і виводимо відповідне повідомлення
} else if (min < 30) {
   alert(`${min} входить в другу чверть`);
} else if (min < 45) {
   alert(`${min} входить в третю чверть`);
} else {
   alert(`${min} входить в четверту чверть`);
}
// Відповідно до значення min виводимо повідомлення про чверть.


let num = prompt("Введіть число від 1 до 4:");
let result;

switch (Number(num)) {
  case 1:
    result = "Зима";
    break;
  case 2:
    result = "Весна";
    break;
  case 3:
    result = "Літо";
    break;
  case 4:
    result = "Осінь";
    break;
  default:
    result = "Вибачте, але ви маєте ввести значення від 1 до 4 включно";
}

console.log(result);
